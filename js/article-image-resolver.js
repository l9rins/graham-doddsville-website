    return '<img src="' + src + '" alt="' + alt.replace(/"/g, '&quot;') + '" style="width:100%;height:auto;max-height:420px;object-fit:cover;border-radius:8px;display:block;margin:0 auto 20px;box-shadow:0 2px 8px #e5e7eb;">';
